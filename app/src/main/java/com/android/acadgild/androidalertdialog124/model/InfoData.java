public class InfoData {

    String keyName;
    int keyId;
    String keyPhone;
    String keyDob;
    String id;

    public int getKeyId() {
        return keyId;
    }

    public void setKeyId(int keyId) {
        this.keyId = keyId;
    }

    public String getKeyName() {
        return keyName;
    }

    public void setKeyName(String keyName) {
        this.keyName = keyName;
    }

    public String getKeyPhone() {
        return keyPhone;
    }

    public void setKeyPhone(String keyPhone) {
        this.keyPhone = keyPhone;
    }

    public String getKeyDob() {
        return keyDob;
    }

    public void setKeyDob(String keyDob) {
        this.keyDob = keyDob;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }


}
